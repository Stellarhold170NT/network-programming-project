    private void btnVerifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerifyActionPerformed
        String verifyCode = txtOTP.getText();
        MessageRequest msg = new MessageRequest();
        msg.setContent(verifyCode);
        clientNetwork.sendMessage(msg);

        MessageResponse registerResult = clientNetwork.readMessage();
        JOptionPane.showMessageDialog(this,
                registerResult.getMessage(),
                registerResult.getStatus(),
                JOptionPane.INFORMATION_MESSAGE);
        
        if (registerResult.getStatus().equals(MessageResponse.SUCCESS)) {
            // Close OTPFrm
            this.dispose();
            // Close RegisterFrm
            if (registerFrm != null) {
                registerFrm.dispose();
            }
            // Show LoginFrm
            if (loginFrm != null) {
                loginFrm.setVisible(true);
                loginFrm.toFront();
            }
        }
    }//GEN-LAST:event_btnVerifyActionPerformed

