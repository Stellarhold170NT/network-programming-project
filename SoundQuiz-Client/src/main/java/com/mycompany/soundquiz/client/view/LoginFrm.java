    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        this.setVisible(false);

        try {
            RegisterFrm registerFrm = new RegisterFrm(this);
            registerFrm.setVisible(true);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this,
                    "Không thể mở cửa sổ đăng ký do lỗi kết nối server:\n" + e.getMessage(),
                    "Lỗi",
                    JOptionPane.ERROR_MESSAGE
            );
            this.setVisible(true); // hiện lại LoginFrm nếu tạo RegisterFrm thất bại
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String email = txtEmail.getText();
        String password = txtPassword.getText();
        
        MessageRequest msg = new MessageRequest();
        msg.setType("login");
        msg.setEmail(email);
        msg.setPassword(password);
        
        clientNetwork.sendMessage(msg);
        
        MessageResponse response = clientNetwork.readMessage();
        
        JOptionPane.showMessageDialog(this,
                response.getMessage(),
                response.getStatus(),
                JOptionPane.INFORMATION_MESSAGE);
        
        if (response.getStatus().equals(MessageResponse.SUCCESS)) {
            
        } else {
            
        }
    }//GEN-LAST:event_btnLoginActionPerformed

