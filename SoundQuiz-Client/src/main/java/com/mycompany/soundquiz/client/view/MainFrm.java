    private void btnConnectServerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectServerActionPerformed
        String ip = txtIP.getText();
        if (!validIpAddress(ip)) {
            JOptionPane.showMessageDialog(this, "Địa chỉ Ip không hợp lệ", "Lỗi nhập liệu", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Integer port;
        try {
            port = Integer.parseInt(txtPort.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Port phải là số nguyên!", "Lỗi nhập liệu", JOptionPane.ERROR_MESSAGE);
            return;
        }

        connectServer(ip, port);
    }//GEN-LAST:event_btnConnectServerActionPerformed

    private void connectServer(String ip, Integer port) {
        System.out.println("Try to connect to server");

        try {
            client.connect(ip, port);
            
            this.setVisible(false);

            java.awt.EventQueue.invokeLater(() -> {
                new LoginFrm().setVisible(true);
            });
            
            
            
        } catch (UnknownHostException e1) {
            JOptionPane.showMessageDialog(this,
                    "Không tìm thấy host: " + ip,
                    "Lỗi kết nối",
                    JOptionPane.ERROR_MESSAGE);

        } catch (ConnectException e2) {
            JOptionPane.showMessageDialog(this,
                    "Không thể kết nối tới server (có thể server chưa chạy).",
                    "Lỗi kết nối",
                    JOptionPane.ERROR_MESSAGE);

        } 
        catch (Exception e3) {
            JOptionPane.showMessageDialog(this,
                    "Lỗi khi kết nối: " + e3.getMessage(),
                    "Lỗi kết nối",
                    JOptionPane.ERROR_MESSAGE);

        }

    }

    public boolean validIpAddress(String ipAddress) {
        String[] numbers = ipAddress.split("\\.");
        if (numbers.length != 4) {
            return false;
        }

        for (String str : numbers) {
            try {
                int i = Integer.parseInt(str);
                if ((i < 0) || (i > 255)) {
                    return false;
                }
            } catch (NumberFormatException e) {
                return false;
            }
        }

        return true;
    }

